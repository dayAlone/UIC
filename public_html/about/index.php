<li>предэкзаменационная подготовка руководителей и специалистов организаций, эксплуатирующих опасные производственные   объекты, подведомственные «Ростехнадзору» в области промышленной, энергетической безопасности и безопасности гидротегических сооружений.</li></ul></p>



