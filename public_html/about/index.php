<li>индивидуально-консультационную форму обучения по профильным специальностям в области Неразрушающего контроля, по программе теоретического обучения с последующей сдачей квалификационного экзамена в Центре;</li>
<li>с выездом на предприятия «Заказчика», при условии комплектования группы не менее 10 человек, предоставления аудитории для проведения теоретического обучения, оборудования для проведения производственного обучения и гостиницы для проживания преподавателей.</li></ul></p>
